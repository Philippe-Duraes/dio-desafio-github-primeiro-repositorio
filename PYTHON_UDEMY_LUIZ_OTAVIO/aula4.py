primeiro_valor = input('Digite o primeiro valor: ')
segundo_valor = input('Digite o segundo valor: ')

if primeiro_valor >= segundo_valor:
    print(f'O primeiro valor é = {primeiro_valor} que é maior ou igual ao segundo valor que é = {segundo_valor}.')
elif segundo_valor > primeiro_valor:
    print(f'O segundo valor é = {segundo_valor} que é maior que o primeiro valor que é = {primeiro_valor}.')    
